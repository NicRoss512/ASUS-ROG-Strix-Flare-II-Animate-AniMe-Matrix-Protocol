def b64(s: str) -> bytes:
    return base64.b64decode("".join(s.split()))

FRAME_A = b64(FRAME_A_B64)
FRAME_B = b64(FRAME_B_B64)
FRAME_CLEAR = bytes([0x60, 0x81]) + b"\x00" * 1022

assert len(FRAME_A) == 1024, len(FRAME_A)
assert len(FRAME_B) == 1024, len(FRAME_B)
assert len(FRAME_CLEAR) == 1024


def open_iface(iface: int = IFACE):
    matches = [d for d in hid.enumerate(VID, PID) if d.get("interface_number") == iface]
    if not matches:
        raise RuntimeError(f"interface {iface} not found")
    h = hid.device()
    h.open_path(matches[0]["path"])
    print(f"opened iface={iface}, path={matches[0]['path']!r}")
    return h


def write_frame(h, payload: bytes, verbose: bool = False):
    # Important: NO leading Report-ID byte. USBPcap showed exactly 1024 bytes to EP 0x07.
    n = h.write(payload)
    if verbose:
        print("write", n, payload[:32].hex(" "))
    if n < 0:
        try:
            err = h.error()
        except Exception:
            err = ""
        raise OSError(f"hid.write failed: {err}")
    if n != len(payload):
        print(f"warning: short write {n}/{len(payload)}")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--iface", type=int, default=IFACE)
    ap.add_argument("--clear", action="store_true", help="send clear frame and exit")
    ap.add_argument("--once", choices=["a", "b"], help="send one captured frame and exit")
    ap.add_argument("--loop", action="store_true", help="alternate captured frames")
    ap.add_argument("--period", type=float, default=0.5)
    ap.add_argument("--verbose", "-v", action="store_true")
    args = ap.parse_args()

    h = open_iface(args.iface)
    try:
        if args.clear:
            write_frame(h, FRAME_CLEAR, args.verbose)
            return
        if args.once:
            write_frame(h, FRAME_A if args.once == "a" else FRAME_B, args.verbose)
            return
        # default: loop
        while True:
            write_frame(h, FRAME_A, args.verbose)
            time.sleep(args.period)
            write_frame(h, FRAME_B, args.verbose)
            time.sleep(args.period)
    except KeyboardInterrupt:
        print("\nstopped")
    finally:
        h.close()


if __name__ == "__main__":
    main()
